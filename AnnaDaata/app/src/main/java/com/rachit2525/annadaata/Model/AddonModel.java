package com.rachit2525.annadaata.Model;

public class AddonModel {

    private String name;
    private Long price;

    public AddonModel() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getPrice() {
        return price;
    }

    public void setPrice(Long price) {
        this.price = price;
    }
}
