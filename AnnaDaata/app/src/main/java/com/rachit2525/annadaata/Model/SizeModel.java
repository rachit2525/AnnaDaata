package com.rachit2525.annadaata.Model;

public class SizeModel {

    private String name;
    private Long prices;

    public SizeModel() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getPrices() {
        return prices;
    }

    public void setPrices(Long prices) {
        this.prices = prices;
    }
}
